public class Operators {
    public static void main(String[] args){
        int a = 10, b=20;

        int sum = a+b;
        int diff = a - b;
        int mul = a*b;

        System.out.println("sum = " + sum);
        System.out.println("diff = " + diff);
        System.out.println("mul = " + mul);

    }
}
